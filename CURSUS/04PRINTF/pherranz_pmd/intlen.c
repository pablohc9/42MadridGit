#include "libftprintf.h"

size_t	intlen(long num)
{
	size_t	c;

	c = 0;
	if (num == 0)
	{
		return (1);
	}
	if (num < 0)
	{
		num *= -1;
		c++;
	}
	while (num > 0)
	{
		num = num / 10;
		c++;
	}
	return (c);
}
